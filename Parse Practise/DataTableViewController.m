@interface DataTableViewController ()

@property (nonatomic) NSArray *data;

@end

@implementation DataTableViewController

@synthesize data;

- (void)viewDidLoad {
    [super viewDidLoad];
    // Uncomment the following line to preserve selection between presentations.
